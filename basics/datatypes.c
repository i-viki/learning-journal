#include <stdio.h>

/* Data Types in C - TN 11th Std Chapter 2 */
int main()
{
    int age = 17;
    float marks = 95.5;
    char grade = 'A';
    double pi = 3.14159;

    printf("Age: %d\n", age);
    printf("Marks: %.1f\n", marks);
    printf("Grade: %c\n", grade);
    printf("Pi: %lf\n", pi);
    return 0;
}
